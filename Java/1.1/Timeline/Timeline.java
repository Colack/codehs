public class Timeline
{
    public static void main(String[] args)
    {
        System.out.println("Age   | M/YY - Description");
        System.out.println("6     | 2/13 - Got my first bicycle");
        printTabs();
        System.out.println("8     | 4/15 - Turned 8");
        printTabs();
        System.out.println("10    | 9/17 - First Day of 5th grade");
        printTabs();
        System.out.println("12    | 9/19 - First Day of 7th Grade");
        printTabs();
        System.out.println("14    | 9/21 - First Day of 9th Grade");
        printTabs();
        System.out.println("16    | 9/23 - First day of 11th grade");
    }
    
    private static void printTabs()
    {   
        System.out.println("      |\n      |\n      |");
    }
}
